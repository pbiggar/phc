int
cmdline_parser_required (struct gengetopt_args_info *args_info, const char *prog_name)
{
  int result = EXIT_SUCCESS;

  if (cmdline_parser_required2(args_info, prog_name, NULL) > 0)
    result = EXIT_FAILURE;

  if (result == EXIT_FAILURE)
    {
      cmdline_parser_free (args_info);
      exit (EXIT_FAILURE);
    }
  
  return result;
}

int
cmdline_parser_required2 (struct gengetopt_args_info *args_info, const char *prog_name, const char *additional_error)
{
  int error = 0;

  /* checks for required options */
  if (check_multiple_option_occurrences(prog_name, args_info->run_given, args_info->run_min, args_info->run_max, "'--run'"))
     error = 1;
  
  if (check_multiple_option_occurrences(prog_name, args_info->r_option_given, args_info->r_option_min, args_info->r_option_max, "'--r-option'"))
     error = 1;
  
  if (check_multiple_option_occurrences(prog_name, args_info->c_option_given, args_info->c_option_min, args_info->c_option_max, "'--c-option' ('-C')"))
     error = 1;
  
  if (check_multiple_option_occurrences(prog_name, args_info->debug_given, args_info->debug_min, args_info->debug_max, "'--debug'"))
     error = 1;
  
  if (check_multiple_option_occurrences(prog_name, args_info->dump_given, args_info->dump_min, args_info->dump_max, "'--dump'"))
     error = 1;
  
  if (check_multiple_option_occurrences(prog_name, args_info->udump_given, args_info->udump_min, args_info->udump_max, "'--udump'"))
     error = 1;
  
  if (check_multiple_option_occurrences(prog_name, args_info->ddump_given, args_info->ddump_min, args_info->ddump_max, "'--ddump'"))
     error = 1;
  
  if (check_multiple_option_occurrences(prog_name, args_info->xdump_given, args_info->xdump_min, args_info->xdump_max, "'--xdump'"))
     error = 1;
  
  if (check_multiple_option_occurrences(prog_name, args_info->disable_given, args_info->disable_min, args_info->disable_max, "'--disable'"))
     error = 1;
  
  
  /* checks for dependences among options */

  return error;
}


static char *package_name = 0;

/**
 * @brief updates an option
 * @param field the generic pointer to the field to update
 * @param orig_field the pointer to the orig field
 * @param field_given the pointer to the number of occurrence of this option
 * @param prev_given the pointer to the number of occurrence already seen
 * @param value the argument for this option (if null no arg was specified)
 * @param possible_values the possible values for this option (if specified)
 * @param default_value the default value (in case the option only accepts fixed values)
 * @param arg_type the type of this option
 * @param check_ambiguity @see cmdline_parser_params.check_ambiguity
 * @param override @see cmdline_parser_params.override
 * @param no_free whether to free a possible previous value
 * @param multiple_option whether this is a multiple option
 * @param long_opt the corresponding long option
 * @param short_opt the corresponding short option (or '-' if none)
 * @param additional_error possible further error specification
 */
static
int update_arg(void *field, char **orig_field,
               unsigned int *field_given, unsigned int *prev_given, 
               char *value, char *possible_values[], const char *default_value,
               cmdline_parser_arg_type arg_type,
               int check_ambiguity, int override,
               int no_free, int multiple_option,
               const char *long_opt, char short_opt,
               const char *additional_error)
{
  char *stop_char = 0;
  const char *val = value;
  int found;
  char **string_field;

  stop_char = 0;
  found = 0;

  if (!multiple_option && prev_given && (*prev_given || (check_ambiguity && *field_given)))
    {
      if (short_opt != '-')
        fprintf (stderr, "%s: `--%s' (`-%c') option given more than once%s\n", 
               package_name, long_opt, short_opt,
               (additional_error ? additional_error : ""));
      else
        fprintf (stderr, "%s: `--%s' option given more than once%s\n", 
               package_name, long_opt,
               (additional_error ? additional_error : ""));
      return 1; /* failure */
    }

    
  if (field_given && *field_given && ! override)
    return 0;
  if (prev_given)
    (*prev_given)++;
  if (field_given)
    (*field_given)++;
  if (possible_values)
    val = possible_values[found];

  switch(arg_type) {
  case ARG_FLAG:
    *((int *)field) = !*((int *)field);
    break;
  case ARG_STRING:
    if (val) {
      string_field = (char **)field;
      if (!no_free && *string_field)
        free (*string_field); /* free previous string */
      *string_field = gengetopt_strdup (val);
    }
    break;
  default:
    break;
  };


  /* store the original value */
  switch(arg_type) {
  case ARG_NO:
  case ARG_FLAG:
    break;
  default:
    if (value && orig_field) {
      if (no_free) {
        *orig_field = value;
      } else {
        if (*orig_field)
          free (*orig_field); /* free previous string */
        *orig_field = gengetopt_strdup (value);
      }
    }
  };

  return 0; /* OK */
}

/**
 * @brief store information about a multiple option in a temporary list
 * @param list where to (temporarily) store multiple options
 */
static
int update_multiple_arg_temp(struct generic_list **list,
               unsigned int *prev_given, const char *val,
               char *possible_values[], const char *default_value,
               cmdline_parser_arg_type arg_type,
               const char *long_opt, char short_opt,
               const char *additional_error)
{
  char *multi_token, *multi_next; /* store single arguments */

  if (arg_type == ARG_NO) {
    (*prev_given)++;
    return 0; /* OK */
  }

  multi_token = get_multiple_arg_token(val);
  multi_next = get_multiple_arg_token_next (val);

  while (1)
    {
      add_node (list);
      if (update_arg((void *)&((*list)->arg), &((*list)->orig), 0,
          prev_given, multi_token, possible_values, default_value, 
          arg_type, 0, 1, 1, 1, long_opt, short_opt, additional_error)) {
        if (multi_token) free(multi_token);
        return 1; /* failure */
      }

      if (multi_next)
        {
          multi_token = get_multiple_arg_token(multi_next);
          multi_next = get_multiple_arg_token_next (multi_next);
        }
      else
        break;
    }

  return 0; /* OK */
}

/**
 * @brief free the passed list (including possible string argument)
 */
static
void free_list(struct generic_list *list, short string_arg)
{
  if (list) {
    struct generic_list *tmp;
    while (list)
      {
        tmp = list;
        if (string_arg && list->arg.string_arg)
          free (list->arg.string_arg);
        if (list->orig)
          free (list->orig);
        list = list->next;
        free (tmp);
      }
  }
}

/**
 * @brief updates a multiple option starting from the passed list
 */
static
void update_multiple_arg(void *field, char ***orig_field,
               unsigned int field_given, unsigned int prev_given, union generic_value *default_value,
               cmdline_parser_arg_type arg_type,
               struct generic_list *list)
{
  int i;
  struct generic_list *tmp;

  if (prev_given && list) {
    *orig_field = (char **) realloc (*orig_field, (field_given + prev_given) * sizeof (char *));

    switch(arg_type) {
    case ARG_STRING:
      *((char ***)field) = (char **)realloc (*((char ***)field), (field_given + prev_given) * sizeof (char *)); break;
    default:
      break;
    };
    
    for (i = (prev_given - 1); i >= 0; --i)
      {
        tmp = list;
        
        switch(arg_type) {
        case ARG_STRING:
          (*((char ***)field))[i + field_given] = tmp->arg.string_arg; break;
        default:
          break;
        }        
        (*orig_field) [i + field_given] = list->orig;
        list = list->next;
        free (tmp);
      }
  } else { /* set the default value */
    if (default_value && ! field_given) {
      switch(arg_type) {
      case ARG_STRING:
        if (! *((char ***)field)) {
          *((char ***)field) = (char **)malloc (sizeof (char *));
          (*((char ***)field))[0] = gengetopt_strdup(default_value->string_arg);
        }
        break;
      default: break;
      }
      if (!(*orig_field)) {
        *orig_field = (char **) malloc (sizeof (char *));
        (*orig_field)[0] = NULL;
      }
    }
  }
}

int
cmdline_parser_internal (int argc, char * const *argv, struct gengetopt_args_info *args_info,
                        struct cmdline_parser_params *params, const char *additional_error)
{
  int c;	/* Character of the parsed option.  */

  struct generic_list * run_list = NULL;
  struct generic_list * r_option_list = NULL;
  struct generic_list * c_option_list = NULL;
  struct generic_list * debug_list = NULL;
  struct generic_list * dump_list = NULL;
  struct generic_list * udump_list = NULL;
  struct generic_list * ddump_list = NULL;
  struct generic_list * xdump_list = NULL;
  struct generic_list * disable_list = NULL;
  int error = 0;
  struct gengetopt_args_info local_args_info;
  
  int override;
  int initialize;
  int check_required;
  int check_ambiguity;
  
  package_name = argv[0];
  
  override = params->override;
  initialize = params->initialize;
  check_required = params->check_required;
  check_ambiguity = params->check_ambiguity;

  if (initialize)
    cmdline_parser_init (args_info);

  cmdline_parser_init (&local_args_info);

  optarg = 0;
  optind = 0;
  opterr = params->print_errors;
  optopt = '?';

  while (1)
    {
      int option_index = 0;

      static struct option long_options[] = {
        { "help",	0, NULL, 'h' },
        { "full-help",	0, NULL, 0 },
        { "version",	0, NULL, 'V' },
        { "verbose",	0, NULL, 'v' },
        { "compile",	0, NULL, 'c' },
        { "pretty-print",	0, NULL, 0 },
        { "obfuscate",	0, NULL, 0 },
        { "run",	1, NULL, 0 },
        { "r-option",	1, NULL, 0 },
        { "read-xml",	1, NULL, 0 },
        { "no-validation",	0, NULL, 0 },
        { "include",	0, NULL, 0 },
        { "c-option",	1, NULL, 'C' },
        { "generate-c",	0, NULL, 0 },
        { "extension",	1, NULL, 0 },
        { "with-php",	1, NULL, 0 },
        { "optimize",	1, NULL, 'O' },
        { "output",	1, NULL, 'o' },
        { "next-line-curlies",	0, NULL, 0 },
        { "no-leading-tab",	0, NULL, 0 },
        { "no-line-numbers",	0, NULL, 0 },
        { "no-nulls",	0, NULL, 0 },
        { "no-empty-lists",	0, NULL, 0 },
        { "tab",	1, NULL, 0 },
        { "no-hash-bang",	0, NULL, 0 },
        { "debug",	1, NULL, 0 },
        { "dump",	1, NULL, 0 },
        { "udump",	1, NULL, 0 },
        { "ddump",	1, NULL, 0 },
        { "xdump",	1, NULL, 0 },
        { "list-passes",	0, NULL, 0 },
        { "dont-fail",	0, NULL, 0 },
        { "no-xml-attrs",	0, NULL, 0 },
        { "disable",	1, NULL, 0 },
        { NULL,	0, NULL, 0 }
      };

      c = getopt_long (argc, argv, "hVvcC:O:o:", long_options, &option_index);

      if (c == -1) break;	/* Exit from `while (1)' loop.  */

      switch (c)
        {
        case 'h':	/* Print help and exit.  */
          cmdline_parser_print_help ();
          cmdline_parser_free (&local_args_info);
          exit (EXIT_SUCCESS);

        case 'V':	/* Print version and exit.  */
          cmdline_parser_print_version ();
          cmdline_parser_free (&local_args_info);
          exit (EXIT_SUCCESS);

        case 'v':	/* Verbose output.  */
        
        
          if (update_arg((void *)&(args_info->verbose_flag), 0, &(args_info->verbose_given),
              &(local_args_info.verbose_given), optarg, 0, 0, ARG_FLAG,
              check_ambiguity, override, 1, 0, "verbose", 'v',
              additional_error))
            goto failure;
        
          break;
        case 'c':	/* Compile.  */
        
        
          if (update_arg((void *)&(args_info->compile_flag), 0, &(args_info->compile_given),
              &(local_args_info.compile_given), optarg, 0, 0, ARG_FLAG,
              check_ambiguity, override, 1, 0, "compile", 'c',
              additional_error))
            goto failure;
        
          break;
        case 'C':	/* Pass option to the C compile (e.g., -C-g; can be specified multiple times).  */
        
          if (update_multiple_arg_temp(&c_option_list, 
              &(local_args_info.c_option_given), optarg, 0, 0, ARG_STRING,
              "c-option", 'C',
              additional_error))
            goto failure;
        
          break;
        case 'O':	/* Optimize.  */
        
        
          if (update_arg( (void *)&(args_info->optimize_arg), 
               &(args_info->optimize_orig), &(args_info->optimize_given),
              &(local_args_info.optimize_given), optarg, 0, "0", ARG_STRING,
              check_ambiguity, override, 0, 0,
              "optimize", 'O',
              additional_error))
            goto failure;
        
          break;
        case 'o':	/* Place executable into file FILE.  */
        
        
          if (update_arg( (void *)&(args_info->output_arg), 
               &(args_info->output_orig), &(args_info->output_given),
              &(local_args_info.output_given), optarg, 0, 0, ARG_STRING,
              check_ambiguity, override, 0, 0,
              "output", 'o',
              additional_error))
            goto failure;
        
          break;

        case 0:	/* Long option with no short option */
          if (strcmp (long_options[option_index].name, "full-help") == 0) {
            cmdline_parser_print_full_help ();
            cmdline_parser_free (&local_args_info);
            exit (EXIT_SUCCESS);
          }

          /* Pretty print input according to the Zend style guidelines.  */
          if (strcmp (long_options[option_index].name, "pretty-print") == 0)
          {
          
          
            if (update_arg((void *)&(args_info->pretty_print_flag), 0, &(args_info->pretty_print_given),
                &(local_args_info.pretty_print_given), optarg, 0, 0, ARG_FLAG,
                check_ambiguity, override, 1, 0, "pretty-print", '-',
                additional_error))
              goto failure;
          
          }
          /* Obfuscate input.  */
          else if (strcmp (long_options[option_index].name, "obfuscate") == 0)
          {
          
          
            if (update_arg((void *)&(args_info->obfuscate_flag), 0, &(args_info->obfuscate_given),
                &(local_args_info.obfuscate_given), optarg, 0, 0, ARG_FLAG,
                check_ambiguity, override, 1, 0, "obfuscate", '-',
                additional_error))
              goto failure;
          
          }
          /* Run the specified plugin (may be specified multiple times).  */
          else if (strcmp (long_options[option_index].name, "run") == 0)
          {
          
            if (update_multiple_arg_temp(&run_list, 
                &(local_args_info.run_given), optarg, 0, 0, ARG_STRING,
                "run", '-',
                additional_error))
              goto failure;
          
          }
          /* Pass option to a plugin (specify multiple flags in the same order as multiple plugins - 1 option only per plugin).  */
          else if (strcmp (long_options[option_index].name, "r-option") == 0)
          {
          
            if (update_multiple_arg_temp(&r_option_list, 
                &(local_args_info.r_option_given), optarg, 0, 0, ARG_STRING,
                "r-option", '-',
                additional_error))
              goto failure;
          
          }
          /* Assume the input is in XML format. Start processing after the named pass.  */
          else if (strcmp (long_options[option_index].name, "read-xml") == 0)
          {
          
          
            if (update_arg( (void *)&(args_info->read_xml_arg), 
                 &(args_info->read_xml_orig), &(args_info->read_xml_given),
                &(local_args_info.read_xml_given), optarg, 0, 0, ARG_STRING,
                check_ambiguity, override, 0, 0,
                "read-xml", '-',
                additional_error))
              goto failure;
          
          }
          /* Toggle XML validation.  */
          else if (strcmp (long_options[option_index].name, "no-validation") == 0)
          {
          
          
            if (update_arg((void *)&(args_info->no_validation_flag), 0, &(args_info->no_validation_given),
                &(local_args_info.no_validation_given), optarg, 0, 0, ARG_FLAG,
                check_ambiguity, override, 1, 0, "no-validation", '-',
                additional_error))
              goto failure;
          
          }
          /* Parse included or required files at compile-time.  */
          else if (strcmp (long_options[option_index].name, "include") == 0)
          {
          
          
            if (update_arg((void *)&(args_info->include_flag), 0, &(args_info->include_given),
                &(local_args_info.include_given), optarg, 0, 0, ARG_FLAG,
                check_ambiguity, override, 1, 0, "include", '-',
                additional_error))
              goto failure;
          
          }
          /* Generate C code.  */
          else if (strcmp (long_options[option_index].name, "generate-c") == 0)
          {
          
          
            if (update_arg((void *)&(args_info->generate_c_flag), 0, &(args_info->generate_c_given),
                &(local_args_info.generate_c_given), optarg, 0, 0, ARG_FLAG,
                check_ambiguity, override, 1, 0, "generate-c", '-',
                additional_error))
              goto failure;
          
          }
          /* Generate a PHP extension called NAME instead of a standalone application.  */
          else if (strcmp (long_options[option_index].name, "extension") == 0)
          {
          
          
            if (update_arg( (void *)&(args_info->extension_arg), 
                 &(args_info->extension_orig), &(args_info->extension_given),
                &(local_args_info.extension_given), optarg, 0, 0, ARG_STRING,
                check_ambiguity, override, 0, 0,
                "extension", '-',
                additional_error))
              goto failure;
          
          }
          /* PHP installation path.  */
          else if (strcmp (long_options[option_index].name, "with-php") == 0)
          {
          
          
            if (update_arg( (void *)&(args_info->with_php_arg), 
                 &(args_info->with_php_orig), &(args_info->with_php_given),
                &(local_args_info.with_php_given), optarg, 0, 0, ARG_STRING,
                check_ambiguity, override, 0, 0,
                "with-php", '-',
                additional_error))
              goto failure;
          
          }
          /* Output the opening curly on the next line instead of on the same line.  */
          else if (strcmp (long_options[option_index].name, "next-line-curlies") == 0)
          {
          
          
            if (update_arg((void *)&(args_info->next_line_curlies_flag), 0, &(args_info->next_line_curlies_given),
                &(local_args_info.next_line_curlies_given), optarg, 0, 0, ARG_FLAG,
                check_ambiguity, override, 1, 0, "next-line-curlies", '-',
                additional_error))
              goto failure;
          
          }
          /* Don't start every line in between <?php .. ?> with a tab.  */
          else if (strcmp (long_options[option_index].name, "no-leading-tab") == 0)
          {
          
          
            if (update_arg((void *)&(args_info->no_leading_tab_flag), 0, &(args_info->no_leading_tab_given),
                &(local_args_info.no_leading_tab_given), optarg, 0, 0, ARG_FLAG,
                check_ambiguity, override, 1, 0, "no-leading-tab", '-',
                additional_error))
              goto failure;
          
          }
          /* Don't show line numbers when dumping DOT/XML.  */
          else if (strcmp (long_options[option_index].name, "no-line-numbers") == 0)
          {
          
          
            if (update_arg((void *)&(args_info->no_line_numbers_flag), 0, &(args_info->no_line_numbers_given),
                &(local_args_info.no_line_numbers_given), optarg, 0, 0, ARG_FLAG,
                check_ambiguity, override, 1, 0, "no-line-numbers", '-',
                additional_error))
              goto failure;
          
          }
          /* Don't show NULLs when dumping DOT.  */
          else if (strcmp (long_options[option_index].name, "no-nulls") == 0)
          {
          
          
            if (update_arg((void *)&(args_info->no_nulls_flag), 0, &(args_info->no_nulls_given),
                &(local_args_info.no_nulls_given), optarg, 0, 0, ARG_FLAG,
                check_ambiguity, override, 1, 0, "no-nulls", '-',
                additional_error))
              goto failure;
          
          }
          /* Don't show empty lists when dumping DOT.  */
          else if (strcmp (long_options[option_index].name, "no-empty-lists") == 0)
          {
          
          
            if (update_arg((void *)&(args_info->no_empty_lists_flag), 0, &(args_info->no_empty_lists_given),
                &(local_args_info.no_empty_lists_given), optarg, 0, 0, ARG_FLAG,
                check_ambiguity, override, 1, 0, "no-empty-lists", '-',
                additional_error))
              goto failure;
          
          }
          /* String to use for tabs while unparsing.  */
          else if (strcmp (long_options[option_index].name, "tab") == 0)
          {
          
          
            if (update_arg( (void *)&(args_info->tab_arg), 
                 &(args_info->tab_orig), &(args_info->tab_given),
                &(local_args_info.tab_given), optarg, 0, "\t", ARG_STRING,
                check_ambiguity, override, 0, 0,
                "tab", '-',
                additional_error))
              goto failure;
          
          }
          /* Do not output any #! lines.  */
          else if (strcmp (long_options[option_index].name, "no-hash-bang") == 0)
          {
          
          
            if (update_arg((void *)&(args_info->no_hash_bang_flag), 0, &(args_info->no_hash_bang_given),
                &(local_args_info.no_hash_bang_given), optarg, 0, 0, ARG_FLAG,
                check_ambiguity, override, 1, 0, "no-hash-bang", '-',
                additional_error))
              goto failure;
          
          }
          /* Print debugging information for the pass named 'passname.  */
          else if (strcmp (long_options[option_index].name, "debug") == 0)
          {
          
            if (update_multiple_arg_temp(&debug_list, 
                &(local_args_info.debug_given), optarg, 0, 0, ARG_STRING,
                "debug", '-',
                additional_error))
              goto failure;
          
          }
          /* Dump input as PHP (although potentially with gotos and labels) after the pass named 'passname'.  */
          else if (strcmp (long_options[option_index].name, "dump") == 0)
          {
          
            if (update_multiple_arg_temp(&dump_list, 
                &(local_args_info.dump_given), optarg, 0, 0, ARG_STRING,
                "dump", '-',
                additional_error))
              goto failure;
          
          }
          /* Dump input as runnable PHP after the pass named 'passname'.  */
          else if (strcmp (long_options[option_index].name, "udump") == 0)
          {
          
            if (update_multiple_arg_temp(&udump_list, 
                &(local_args_info.udump_given), optarg, 0, 0, ARG_STRING,
                "udump", '-',
                additional_error))
              goto failure;
          
          }
          /* Dump input as DOT after the pass named 'passname'.  */
          else if (strcmp (long_options[option_index].name, "ddump") == 0)
          {
          
            if (update_multiple_arg_temp(&ddump_list, 
                &(local_args_info.ddump_given), optarg, 0, 0, ARG_STRING,
                "ddump", '-',
                additional_error))
              goto failure;
          
          }
          /* Dump input as XML after the pass named 'passname'.  */
          else if (strcmp (long_options[option_index].name, "xdump") == 0)
          {
          
            if (update_multiple_arg_temp(&xdump_list, 
                &(local_args_info.xdump_given), optarg, 0, 0, ARG_STRING,
                "xdump", '-',
                additional_error))
              goto failure;
          
          }
          /* List the passes to be run.  */
          else if (strcmp (long_options[option_index].name, "list-passes") == 0)
          {
          
          
            if (update_arg((void *)&(args_info->list_passes_flag), 0, &(args_info->list_passes_given),
                &(local_args_info.list_passes_given), optarg, 0, 0, ARG_FLAG,
                check_ambiguity, override, 1, 0, "list-passes", '-',
                additional_error))
              goto failure;
          
          }
          /* Dont fail on error (after parsing).  */
          else if (strcmp (long_options[option_index].name, "dont-fail") == 0)
          {
          
          
            if (update_arg((void *)&(args_info->dont_fail_flag), 0, &(args_info->dont_fail_given),
                &(local_args_info.dont_fail_given), optarg, 0, 0, ARG_FLAG,
                check_ambiguity, override, 1, 0, "dont-fail", '-',
                additional_error))
              goto failure;
          
          }
          /* When dumping XML, omit node attributes.  */
          else if (strcmp (long_options[option_index].name, "no-xml-attrs") == 0)
          {
          
          
            if (update_arg((void *)&(args_info->no_xml_attrs_flag), 0, &(args_info->no_xml_attrs_given),
                &(local_args_info.no_xml_attrs_given), optarg, 0, 0, ARG_FLAG,
                check_ambiguity, override, 1, 0, "no-xml-attrs", '-',
                additional_error))
              goto failure;
          
          }
          /* Disable the pass named 'passname'.  */
          else if (strcmp (long_options[option_index].name, "disable") == 0)
          {
          
            if (update_multiple_arg_temp(&disable_list, 
                &(local_args_info.disable_given), optarg, 0, 0, ARG_STRING,
                "disable", '-',
                additional_error))
              goto failure;
          
          }
          
          break;
        case '?':	/* Invalid option.  */
          /* `getopt_long' already printed an error message.  */
          goto failure;

        default:	/* bug: option not considered.  */
          fprintf (stderr, "%s: option unknown: %c%s\n", CMDLINE_PARSER_PACKAGE, c, (additional_error ? additional_error : ""));
          abort ();
        } /* switch */
    } /* while */


  update_multiple_arg((void *)&(args_info->run_arg),
    &(args_info->run_orig), args_info->run_given,
    local_args_info.run_given, 0 , 
    ARG_STRING, run_list);
  update_multiple_arg((void *)&(args_info->r_option_arg),
    &(args_info->r_option_orig), args_info->r_option_given,
    local_args_info.r_option_given, 0 , 
    ARG_STRING, r_option_list);
  update_multiple_arg((void *)&(args_info->c_option_arg),
    &(args_info->c_option_orig), args_info->c_option_given,
    local_args_info.c_option_given, 0 , 
    ARG_STRING, c_option_list);
  update_multiple_arg((void *)&(args_info->debug_arg),
    &(args_info->debug_orig), args_info->debug_given,
    local_args_info.debug_given, 0 , 
    ARG_STRING, debug_list);
  update_multiple_arg((void *)&(args_info->dump_arg),
    &(args_info->dump_orig), args_info->dump_given,
    local_args_info.dump_given, 0 , 
    ARG_STRING, dump_list);
  update_multiple_arg((void *)&(args_info->udump_arg),
    &(args_info->udump_orig), args_info->udump_given,
    local_args_info.udump_given, 0 , 
    ARG_STRING, udump_list);
  update_multiple_arg((void *)&(args_info->ddump_arg),
    &(args_info->ddump_orig), args_info->ddump_given,
    local_args_info.ddump_given, 0 , 
    ARG_STRING, ddump_list);
  update_multiple_arg((void *)&(args_info->xdump_arg),
    &(args_info->xdump_orig), args_info->xdump_given,
    local_args_info.xdump_given, 0 , 
    ARG_STRING, xdump_list);
  update_multiple_arg((void *)&(args_info->disable_arg),
    &(args_info->disable_orig), args_info->disable_given,
    local_args_info.disable_given, 0 , 
    ARG_STRING, disable_list);

  args_info->run_given += local_args_info.run_given;
  local_args_info.run_given = 0;
  args_info->r_option_given += local_args_info.r_option_given;
  local_args_info.r_option_given = 0;
  args_info->c_option_given += local_args_info.c_option_given;
  local_args_info.c_option_given = 0;
  args_info->debug_given += local_args_info.debug_given;
  local_args_info.debug_given = 0;
  args_info->dump_given += local_args_info.dump_given;
  local_args_info.dump_given = 0;
  args_info->udump_given += local_args_info.udump_given;
  local_args_info.udump_given = 0;
  args_info->ddump_given += local_args_info.ddump_given;
  local_args_info.ddump_given = 0;
  args_info->xdump_given += local_args_info.xdump_given;
  local_args_info.xdump_given = 0;
  args_info->disable_given += local_args_info.disable_given;
  local_args_info.disable_given = 0;
  
  if (check_required)
    {
      error += cmdline_parser_required2 (args_info, argv[0], additional_error);
    }

  cmdline_parser_release (&local_args_info);

  if ( error )
    return (EXIT_FAILURE);

  if (optind < argc)
    {
      int i = 0 ;
      int found_prog_name = 0;
      /* whether program name, i.e., argv[0], is in the remaining args
         (this may happen with some implementations of getopt,
          but surely not with the one included by gengetopt) */

      i = optind;
      while (i < argc)
        if (argv[i++] == argv[0]) {
          found_prog_name = 1;
          break;
        }
      i = 0;

      args_info->inputs_num = argc - optind - found_prog_name;
      args_info->inputs =
        (char **)(malloc ((args_info->inputs_num)*sizeof(char *))) ;
      while (optind < argc)
        if (argv[optind++] != argv[0])
          args_info->inputs[ i++ ] = gengetopt_strdup (argv[optind-1]) ;
    }

  return 0;

failure:
  free_list (run_list, 1 );
  free_list (r_option_list, 1 );
  free_list (c_option_list, 1 );
  free_list (debug_list, 1 );
  free_list (dump_list, 1 );
  free_list (udump_list, 1 );
  free_list (ddump_list, 1 );
  free_list (xdump_list, 1 );
  free_list (disable_list, 1 );
  
  cmdline_parser_release (&local_args_info);
  return (EXIT_FAILURE);
}
